// code demonstration of if-else statement
#include <iostream>
using namespace std;
int main()
{

    int num = 4;
    if (num == 2)
    {   // if block
        cout << "We are under the if block" << endl;
    }
    else
    {   // else block
        cout << "We are under the else block" << endl;
    }
    return 0;
}