// code demonstration of if-else-if ladder
#include <iostream>
using namespace std;
int main()
{

    int value = 4;
    if (value == 23)
    {
        cout << "Good Morning dear" << endl;
    }
    else if (value == 3)
    {
        cout << "Good Afternoon dear" << endl;
    }
    else
    {
        cout << "Good Evening dear" << endl;
    }
    return 0;
}