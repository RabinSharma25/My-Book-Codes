// code showing the use of relational operators
#include <iostream>
using namespace std;
int main()
{

    int val1 = 4 == 4; // true
    int val2 = 4 != 4; // flase
    int val3 = 4 > 4;  // false
    int val4 = 4 < 4;  // false
    int val5 = 4 >= 4; // true
    int val6 = 4 <= 4; // ture
    cout << "value of val1 is " << val1 << endl;
    cout << "value of val2 is " << val2 << endl;
    cout << "value of val3 is " << val3 << endl;
    cout << "value of val4 is " << val4 << endl;
    cout << "value of val5 is " << val5 << endl;
    cout << "value of val6 is " << val6 << endl;
    return 0;
}