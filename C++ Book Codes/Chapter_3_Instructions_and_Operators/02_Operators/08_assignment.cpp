// Code showing the use of assignment operators
#include<iostream>
using namespace std;
int main(){

int var1;
var1 = 5; // here value of var1 is assigned to 5;

int var2 = 4;
var2 += 4; // same as var2 = var2 + 4;

int var3 = 4;
var3 -= 4; // same as var3 = var3 - 4;

int var4 = 4;
var4 *= 4; // same as var4 = var4 * 4;

int var5 = 3;
var5 /= 4; // same as var4 = var4 / 4;

    return 0;
}